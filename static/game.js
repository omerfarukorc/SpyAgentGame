// Baƒülantƒ± kuruldu
socket.on('connect', () => {
    console.log('Oyun sunucusuna baƒülanƒ±ldƒ±');
    connectionState.isConnected = true;
    connectionState.isReconnecting = false;
    connectionState.reconnectAttempts = 0;
    
    updateConnectionStatus(true);
    startKeepAlive();
    
    // Reconnection ise durumu bildir
    if (connectionState.lastDisconnectTime) {
        const disconnectDuration = Date.now() - connectionState.lastDisconnectTime;
        if (disconnectDuration > 2000) { // 2 saniyeden uzun disconnect
            showNotification('Baƒülantƒ± yeniden kuruldu! üéâ', 'success');
        }
        connectionState.lastDisconnectTime = null;
    }
    
    // Oyuncunun adƒ±nƒ± localStorage'dan al
    const savedPlayerName = localStorage.getItem('playerName');
    if (savedPlayerName) {
        gameState.playerName = savedPlayerName;
        
        // Kaydedilen oyun durumunu y√ºkle
        loadGameState();
        
        // Otomatik olarak odaya katƒ±l
        socket.emit('join_room', {
            room_id: gameState.roomId,
            player_name: savedPlayerName,
            reconnect: true,
            game_state: {
                was_in_game: connectionState.wasGameStarted,
                current_phase: currentPhase
            }
        });
    } else {
        // Eƒüer kayƒ±tlƒ± isim yoksa ana sayfaya y√∂nlendir
        window.location.href = '/?room=' + gameState.roomId;
    }
});

// Baƒülantƒ± koptu
socket.on('disconnect', (reason) => {
    console.log('Baƒülantƒ± koptu:', reason);
    connectionState.isConnected = false;
    connectionState.lastDisconnectTime = Date.now();
    
    updateConnectionStatus(false);
    stopKeepAlive();
    
    // Oyun durumunu kaydet
    saveGameState();
    
    // Mobil cihazlarda otomatik reconnect i√ßin farklƒ± mesaj
    if (reason === 'transport close' || reason === 'ping timeout') {
        showNotification('Baƒülantƒ± koptu. Yeniden baƒülanƒ±yor...', 'warning');
    } else {
        showNotification('Sunucu baƒülantƒ±sƒ± koptu!', 'error');
    }
});

