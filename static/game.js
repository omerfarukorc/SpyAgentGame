const playerColors = new Map();

function getPlayerColor(playerName) {
    if (!playerColors.has(playerName)) {
        // Deterministik renk √ºretimi (isimden hash)
        let hash = 0;
        for (let i = 0; i < playerName.length; i++) {
            hash = playerName.charCodeAt(i) + ((hash << 5) - hash);
        }
        
        // HSL formatƒ±nda renk √ºret (daha canlƒ± renkler i√ßin)
        const hue = Math.abs(hash) % 360;
        const saturation = 60 + (Math.abs(hash) % 40); // 60-100 arasƒ±
        const lightness = 45 + (Math.abs(hash) % 20);  // 45-65 arasƒ±
        
        const color = `hsl(${hue}, ${saturation}%, ${lightness}%)`;
        playerColors.set(playerName, color);
    }
    
    return playerColors.get(playerName);
}

