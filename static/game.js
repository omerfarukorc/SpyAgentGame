// Otomatik reconnection i√ßin oyun durumunu kaydet
function saveGameState() {
    const gameData = {
        roomId: gameState.roomId,
        playerName: gameState.playerName,
        gameStarted: gameState.gameStarted,
        myRole: gameState.myRole,
        currentPhase: currentPhase,
        timestamp: Date.now()
    };
    localStorage.setItem('gameState_' + gameState.roomId, JSON.stringify(gameData));
}

// Kaydedilen oyun durumunu y√ºkle
function loadGameState() {
    const savedData = localStorage.getItem('gameState_' + gameState.roomId);
    if (savedData) {
        try {
            const gameData = JSON.parse(savedData);
            // 20 dakikadan eski veriler ge√ßersiz (uzun ekran kapatma i√ßin)
            if (Date.now() - gameData.timestamp < 20 * 60 * 1000) {
                gameState.gameStarted = gameData.gameStarted;
                gameState.myRole = gameData.myRole;
                currentPhase = gameData.currentPhase;
                connectionState.wasGameStarted = gameData.gameStarted;
                
                // Rol bilgisini hatƒ±rla ve gizlenmi≈ü ≈üekilde g√∂ster
                if (gameData.myRole && gameData.gameStarted) {
                    setTimeout(() => {
                        hideRole(); // Direkt gizlenmi≈ü ≈üekilde ba≈ülat
                    }, 1000);
                }
                
                return true;
            }
        } catch (e) {
            console.error('Game state y√ºklenirken hata:', e);
        }
    }
    return false;
}

// Keep-alive ping g√∂nder
function startKeepAlive() {
    if (keepAliveInterval) clearInterval(keepAliveInterval);
    
    keepAliveInterval = setInterval(() => {
        if (connectionState.isConnected) {
            socket.emit('ping', { roomId: gameState.roomId });
            console.log('Keep-alive ping sent');
        }
    }, 45000); // 45 saniyede bir ping (daha az sƒ±klƒ±k)
}

function stopKeepAlive() {
    if (keepAliveInterval) {
        clearInterval(keepAliveInterval);
        keepAliveInterval = null;
    }
}

// Visibility API - sayfa durumunu izle
function handleVisibilityChange() {
    if (document.hidden) {
        isPageVisible = false;
        console.log('Sayfa arka plana gitti - baƒülantƒ± korunuyor');
        
        // Arka planda da baƒülantƒ±yƒ± koru, sadece UI g√ºncellemelerini durdur
        // Keep-alive devam etsin (ping g√∂nderimi s√ºrs√ºn)
        
    } else {
        isPageVisible = true;
        console.log('Sayfa √∂n plana geldi');
        
        // Baƒülantƒ±yƒ± kontrol et
        if (!connectionState.isConnected && !connectionState.isReconnecting) {
            console.log('Sayfa geri geldi, baƒülantƒ± kopuk - yeniden baƒülanmaya √ßalƒ±≈üƒ±yor...');
            showNotification('Yeniden baƒülanƒ±yor...', 'info');
            socket.connect();
        }
        
        // Keep-alive zaten √ßalƒ±≈üƒ±yor olmalƒ± ama emin olmak i√ßin
        if (!keepAliveInterval) {
            startKeepAlive();
        }
    }
}

// Visibility API'yi dinle
if (typeof document.hidden !== "undefined") {
    document.addEventListener("visibilitychange", handleVisibilityChange);
} else if (typeof document.msHidden !== "undefined") {
    document.addEventListener("msvisibilitychange", handleVisibilityChange);
} else if (typeof document.webkitHidden !== "undefined") {
    document.addEventListener("webkitvisibilitychange", handleVisibilityChange);
} 